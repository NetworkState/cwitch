struct NAME_TLV
{
	UINT32 type;
	BUFFER data;
};

template <typename STREAM>
void WriteNameTLV(STREAM&& outStream, NAME_TLV& tlv)
{
	outStream.writeInt(tlv.type);
	outStream.writeInt(tlv.data.length());
	outStream.writeStream(tlv.data);
}

void ReadNameTLV(BUFFER& data, NAME_TLV& tlv)
{
	tlv.type = data.read<UINT32>();
	auto length = data.read<UINT32>();
	tlv.data = data.readBytes(length);
}

#define DICT_NAME_HEADER	1
#define DICT_VARIANT_HEADER	2
#define DICT_EXTENSION_HEADER	3
#define DICT_SORT_HEADER	4
#define DICT_EXTENSION_DATA	5

template<typename DICT>
bool LoadDictionary(DICT&& dictionary, USTRING filename)
{
	TSTRINB_BUILDER byteStream;
	byteStream.readFile(filename);

	auto buffer = byteStream.toBuffer();
	auto result = buffer.length() > 0;
	while (buffer)
	{
		NAME_TLV tlv;
		ReadNameTLV(buffer, tlv);
		ASSERT(tlv.data);

		auto tlvLength = tlv.data.length();
		auto tlvData = tlv.data.data();

		switch (tlv.type)
		{
		case DICT_NAME_HEADER:
		{
			auto count = (INT32)(tlvLength / sizeof(NAME_HEADER));
			auto address = dictionary.nameHeaders.commit(count);
			RtlCopyMemory((PUINT8)address, tlvData, tlvLength);
			break;
		}

		case DICT_VARIANT_HEADER:
		{
			auto count = (INT32)(tlvLength / sizeof(VARIANT_HEADER));
			auto address = dictionary.variantHeaders.commit(count);
			RtlCopyMemory((PUINT8)address, tlvData, tlvLength);
			break;
		}

		case DICT_EXTENSION_HEADER:
		{
			auto count = (INT32)(tlvLength / sizeof(EXTENSION_HEADER));
			auto address = dictionary.extensionHeaders.commit(count);
			RtlCopyMemory(address, tlvData, tlvLength);
			break;
		}

		case DICT_SORT_HEADER:
		{
			auto count = (INT32)(tlvLength / sizeof(SORT_HEADER));
			auto address = dictionary.sortHeaders.commit(count);
			RtlCopyMemory(address, tlvData, tlvLength);
			break;
		}

		case DICT_EXTENSION_DATA:
		{
			ASSERT(dictionary.extensionData == nullptr);
			dictionary.extensionData = (PUINT8)ExAllocatePoolWithTag(EXTENSION_DATA_SIZE);
			RtlZeroMemory(dictionary.extensionData, EXTENSION_DATA_SIZE);
			dictionary.extensionDataWriteOffset = tlvLength;
			RtlCopyMemory(dictionary.extensionData, tlvData, tlvLength);
			break;
		}

		default:
			DBGBREAK();
		}
	}
	return result;
}

template <typename DICT>
void SaveDictionary(DICT&& dictionary, USTRING filename)
{
	TSTRING_BUILDER outStream;
	outStream.reserve(256 * 1024);

	NAME_TLV tlv = { DICT_NAME_HEADER, BUFFER((PUINT8)dictionary.nameHeaders.address(), (UINT32)(dictionary.nameHeaders.count() * sizeof(NAME_HEADER))) };
	WriteNameTLV(outStream, tlv);

	tlv = { DICT_VARIANT_HEADER, BUFFER((PUINT8)dictionary.variantHeaders.address(), (UINT32)(dictionary.variantHeaders.count() * sizeof(VARIANT_HEADER))) };
	WriteNameTLV(outStream, tlv);

	tlv = { DICT_EXTENSION_HEADER, BUFFER((PUINT8)dictionary.extensionHeaders.address(),  (UINT32)(dictionary.extensionHeaders.count() * sizeof(EXTENSION_HEADER))) };
	WriteNameTLV(outStream, tlv);

	tlv = { DICT_SORT_HEADER, BUFFER((PUINT8)dictionary.sortHeaders.address() , (UINT32)(dictionary.sortHeaders.count() * sizeof(SORT_HEADER))) };
	WriteNameTLV(outStream, tlv);

	tlv = { DICT_EXTENSION_DATA, BUFFER((PUINT8)dictionary.extensionData, (UINT32)(dictionary.extensionDataWriteOffset)) };
	WriteNameTLV(outStream, tlv);

	WriteFile(filename, outStream.toBuffer());
}

static void WriteDefineStatement(TSTRING_BUILDER& defineString, USTRING prefix, USTRING name, TOKEN index, bool caseSensitive = false)
{
	char buffer[256];
	char tempName[256];
	RtlZeroMemory(tempName, sizeof(tempName));
	if (name == USTRING("*"))
	{
		strncpy_s(tempName, "STAR", sizeof(tempName));
	}
	else if (name == USTRING("/"))
	{
		strncpy_s(tempName, "SLASH", sizeof(tempName));
	}
	else
	{
		for (UINT32 i = 0; i < name.length(); i++)
		{
			tempName[i] = caseSensitive ? name[i] : (UINT8)toupper(name[i]);
			if ((name[i] == '-') || (name[i] == ' ') || (name[i] == '!') || (name[i] == '/') || (name[i] == '+') || (name[i] == '.'))
				tempName[i] = '_';
		}
	}

	RtlStringCchPrintfA(buffer, 256, "constexpr auto %s_%s = NAME_HANDLE(0x%08Xu);\r\n", prefix.data(), tempName, (index._type << 24) | index.getValue());
	defineString.writeString(buffer);
}

static void WriteDefineStatement(TSTRING_BUILDER& defineString, USTRING name, TOKEN index)
{
	char buffer[256];
	RtlStringCchPrintfA(buffer, 256, "constexpr auto %s = NAME_HANDLE(0x%08Xu);\r\n", name.data(), (index._type << 24) | index.getValue());
	defineString.writeString(buffer);
}

constexpr USTRING DEFINE_FILENAME = "define.txt";
constexpr UINT32 DEFINE_BUFFER_SIZE = 128 * 1024 * 1024;
constexpr UINT32 ARRAY_BUFFER_SIZE = 2 * 1024 * 1024;

UINT8* defineBuffer;
void PopulateNames()
{
	TSTRING_BUILDER defineString;
	/*
	if (LoadDictionary(BaseNameDictionary, BASE_NAME_DICTIONARY_FILENAME) == true)
	{
		return;
	}
	*/
	defineBuffer = (UINT8*)KernelAlloc(DEFINE_BUFFER_SIZE);

	defineString.setBuffer(defineBuffer, DEFINE_BUFFER_SIZE);

	for (auto& prefixName : NameStrings)
	{
		TOKEN nameToken = CreateName(prefixName.name, true);
		WriteDefineStatement(defineString, prefixName.prefix, prefixName.name, nameToken, true);
	}

	for (auto& nameValue : NameValueStrings)
	{
		TOKEN nameToken = CreateName(nameValue.value, true);
		WriteDefineStatement(defineString, nameValue.name, nameToken);
	}

	WriteFile(DEFINE_FILENAME, defineString.toBuffer());


	ExFreePoolWithTag(defineBuffer, POOL_TAG);

	SaveDictionary(GlobalStack().dictionary, BASE_NAME_DICTIONARY_FILENAME);
}
